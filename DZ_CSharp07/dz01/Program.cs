//Задача 47. Задайте двумерный массив размером m×n, заполненный случайными вещественными числами.
// m = 3, n = 4.
//0,5 7 -2 -0,2
//1 -3,3 8 -9,9
//8 7,8 -7,1 9




int lines = ConsoleInput("Введите количество строк  ");
int columns = ConsoleInput("Введите количество столбцов ");
double[,] numbers = new double[lines, columns];
ArrayRandomNumbers(numbers);
ConsoleArray(numbers);

void ArrayRandomNumbers(double[,] array)
{
    for (int i = 0; i < array.GetLength(0); i++)
    {
        for (int j = 0; j < array.GetLength(1); j++)
        {
            array[i, j] = Convert.ToDouble(new Random().Next(-100, 100)) / 10;
        }
    }
}

void ConsoleArray(double[,] array)
{
    for (int i = 0; i < array.GetLength(0); i++)
    {
        Console.Write("[ ");
        for (int j = 0; j < array.GetLength(1); j++)
        {
            Console.Write(array[i, j] + " ");
        }
        Console.Write("]");
        Console.WriteLine("");
    }
}




int ConsoleInput(string message)  
{
    Console.Write(message);
    return Convert.ToInt32(Console.ReadLine());
}